import React from 'react'
import ReactDOM from 'react-dom'
import {
  BrowserRouter as Router,
  Route,
  Switch,
  Redirect,
} from 'react-router-dom'

import './style.css'
import Home from './views/home'
import TutoringPrograms from './views/tutoring-programs'
import FoundationSoftSkills from './views/foundation-soft-skills'
import NotFound from './views/not-found'

const App = () => {
  return (
    <Router>
      <Switch>
        <Route component={Home} exact path="/" />
        <Route component={TutoringPrograms} exact path="/tutoring-programs" />
        <Route
          component={FoundationSoftSkills}
          exact
          path="/foundation-soft-skills"
        />
        <Route component={NotFound} path="**" />
        <Redirect to="**" />
      </Switch>
    </Router>
  )
}

ReactDOM.render(<App />, document.getElementById('app'))
